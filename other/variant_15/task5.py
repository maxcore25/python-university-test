import math


def main(z, y, x):
    res = 0
    n = len(x)
    for i in range(1, n + 1):
        a = x[math.ceil(i / 4) - 1] ** 2
        b = z[n - math.ceil(i / 3)] ** 3
        c = 63 * y[n - i]
        res += 31 * math.log(a - b - c, 10)
    return "%.2e" % (92 * res)


