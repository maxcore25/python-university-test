from math import *


def main(z, x, y):
    total = 0
    n = len(z)

    for i in range(1, n + 1):
        total += 8 * (y[n - i] + x[i - 1] ** 3 + z[n - ceil(i / 3)] ** 2) ** 5

    return 66 * total


