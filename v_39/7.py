def main(inp):
    a = (inp & 0b00000000000000000000000000000111) << 2
    b = (inp & 0b00000000000000000000011111111000) << 2
    c = (inp & 0b00000000000000000011100000000000) << 2
    d = (inp & 0b00000000000000001100000000000000) >> 14
    e = (inp & 0b00011111111111110000000000000000) << 3
    f = (inp & 0b00100000000000000000000000000000) >> 13
    g = (inp & 0b11000000000000000000000000000000) >> 13

    return a | b | c | d | e | f | g


