from math import *


def main(y, z, x):
    total = 0
    n = len(x)

    for i in range(1, n + 1):
        total += z[n - ceil(i / 4)] ** 2 + y[ceil(i / 4) - 1] + 16 * x[i - 1] ** 3

    return '%.2e' % total


