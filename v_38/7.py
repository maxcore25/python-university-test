def main(inp):
    a = (inp & 0b00000000000000000000000000000001) << 27
    b = (inp & 0b00000000000000000000000000000010) << 27
    c = (inp & 0b00000000000000111111111111111100) << 1
    d = (inp & 0b00000000000011000000000000000000) << 11
    e = (inp & 0b00000000000100000000000000000000) << 11
    f = (inp & 0b00011111111000000000000000000000) >> 2
    g = (inp & 0b11100000000000000000000000000000) >> 29

    return a | b | c | d | e | f | g


